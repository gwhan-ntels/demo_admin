package com.ntels.ccbs.system.domain.common.service;

public class MaskRule {

	private String soId; /* 사업ID */
	private String fieldName; /* 필드명(CamelCase) */
	private int startMaskIndex; /* 마스크처리 시작 INDEX */
	private int endMaskIndex; /* 마스크처리 종료 INDEX(9999 세팅시 문자열 끝까지 적용) */
	private String maskChar; /* 처리문자 */
	public String getSoId() {
		return soId;
	}
	public void setSoId(String soId) {
		this.soId = soId;
	}
	public String getFieldName() {
		return fieldName;
	}
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}
	public int getStartMaskIndex() {
		return startMaskIndex;
	}
	public void setStartMaskIndex(int startMaskIndex) {
		this.startMaskIndex = startMaskIndex;
	}
	public int getEndMaskIndex() {
		return endMaskIndex;
	}
	public void setEndMaskIndex(int endMaskIndex) {
		this.endMaskIndex = endMaskIndex;
	}
	public String getMaskChar() {
		return maskChar;
	}
	public void setMaskChar(String maskChar) {
		this.maskChar = maskChar;
	}
	@Override
    public String toString() {
	    return "MaskRule [soId=" + soId + ", fieldName=" + fieldName
	            + ", startMaskIndex=" + startMaskIndex + ", endMaskIndex="
	            + endMaskIndex + ", maskChar=" + maskChar + "]";
    }
	
	
}
