package com.ntels.ccbs.charge.domain.charge.batch;

import java.io.Serializable;

import com.ntels.ccbs.system.domain.common.service.CommonVO;

public class BatchProgramVO extends BatchCommonVO implements Serializable, CommonVO {

	/**
	 * 프로그램ID
	 */
	private String batPgmId;

	/**
	 * 프로그램명
	 */
	private String pgmNm;

	/**
	 * 실행Object
	 */
	private String execObj;

	/**
	 * 마감정보설정구분
	 */
	private String clsStpCl;

	/**
	 * BLIV030 01: 계산작업완료 02: 청구작업완료 03: 수납작업완료 04: 미납작업완료 05: 회계매출마감 06: 정산작업완료
	 */
	private String clsTskCl;

	/**
	 * 프로그램개요
	 */
	private String pgmCt;

	/**
	 * 패키지 이름
	 */
	private String pkgNm;

	private String clsStpNm;
	private String clsTskNm;

	public String getBatPgmId() {
		return batPgmId;
	}

	public void setBatPgmId(String batPgmId) {
		this.batPgmId = batPgmId;
	}

	public String getPgmNm() {
		return pgmNm;
	}

	public void setPgmNm(String pgmNm) {
		this.pgmNm = pgmNm;
	}

	public String getExecObj() {
		return execObj;
	}

	public void setExecObj(String execObj) {
		this.execObj = execObj;
	}

	public String getClsStpCl() {
		return clsStpCl;
	}

	public void setClsStpCl(String clsStpCl) {
		this.clsStpCl = clsStpCl;
	}

	public String getClsTskCl() {
		return clsTskCl == null ? "" : clsTskCl.trim();
	}

	public void setClsTskCl(String clsTskCl) {
		this.clsTskCl = clsTskCl;
	}

	public String getPgmCt() {
		return pgmCt;
	}

	public void setPgmCt(String pgmCt) {
		this.pgmCt = pgmCt;
	}

	public String getPkgNm() {
		return pkgNm;
	}

	public void setPkgNm(String pkgNm) {
		this.pkgNm = pkgNm;
	}

	public String getClsStpNm() {
		return clsStpNm;
	}

	public String getClsTskNm() {
		return clsTskNm;
	}

	public void setClsStpNm(String clsStpNm) {
		this.clsStpNm = clsStpNm;
	}

	public void setClsTskNm(String clsTskNm) {
		this.clsTskNm = clsTskNm;
	}

}
